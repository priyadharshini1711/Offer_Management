    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int flag = 0;
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "select date to update");
            flag = 1;
        }

        if (flag == 0) {
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy", Locale.getDefault());
            String a = sdf.format(jDateChooser1.getDate());
            List<Integer> ids = new ArrayList<Integer>();
            for (int count = 0; count < model.getRowCount(); count++) {
                System.out.println(model.getValueAt(count, 0).toString());
                if (model.getValueAt(count, 0).toString().equals("true")) {
                    ids.add(Integer.parseInt((String) model.getValueAt(count, 1)));
                }
            }
            if (ids.size() == 0) {
                JOptionPane.showMessageDialog(rootPane, "No Rows Selected");
            } else {
                try {
                    Class.forName("com.mysql.jdbc.Driver");
                    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/offer_management?characterEncoding=utf8", "root", "");
                    PreparedStatement preparedStatement = con.prepareStatement("update offer_details set end_date = ? where id = ?");
                    for (int id : ids) {

                        preparedStatement.setString(1, a);
                        preparedStatement.setInt(2, id);
                        preparedStatement.addBatch();
                    }

                    preparedStatement.executeBatch();
                    populateTable();

                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(UpdateOffer.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(UpdateOffer.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        }

    }//GEN-LAST:event_jButton2ActionPerformed

