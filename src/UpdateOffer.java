    private void populateTable() {
        try {

            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/offer_management?characterEncoding=utf8", "root", "");
            PreparedStatement preparedStatement = con.prepareStatement("select * from offer_details");
            ResultSet resultSet = preparedStatement.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            while (resultSet.next()) {
                String c = String.valueOf(resultSet.getInt(1));
                String d = String.valueOf(resultSet.getString(2));
                String e = String.valueOf(resultSet.getString(3));
                String f = String.valueOf(resultSet.getString(4));
                String g = String.valueOf(resultSet.getString(5));
                Object tableRow[] = {false, c, d, e, f, g};
                DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
                tableModel.addRow(tableRow);
                jTable1.setAutoCreateRowSorter(true);
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UpdateOffer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(UpdateOffer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
